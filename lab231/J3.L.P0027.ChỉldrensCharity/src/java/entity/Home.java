package entity;

import java.util.Date;

/**
 *
 * @author Admin
 */
public class Home {

    int id;
    String title, img, content;
    Date date;
    String link;

    public Home() {
    }

    public Home(int id, String title, String img, String content, Date date, String link) {
        this.id = id;
        this.title = title;
        this.img = img;
        this.content = content;
        this.date = date;
        this.link = link;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

}
