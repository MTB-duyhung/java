package model;

/**
 *
 * @author Admin
 */
public class aboutMe {
    String detail, name;

    public aboutMe() {
    }

    public aboutMe(String detail, String name) {
        this.detail = detail;
        this.name = name;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
