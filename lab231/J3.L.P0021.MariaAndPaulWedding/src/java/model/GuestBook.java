package model;

import java.util.Date;

/**
 *
 * @author Admin
 */
public class GuestBook {

    private int id;
    private String name, img, content;
    private Date date;

    public GuestBook() {
    }

    public GuestBook(int id, String name, String img, String content, Date date) {
        this.id = id;
        this.name = name;
        this.img = img;
        this.content = content;
        this.date = date;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

}
