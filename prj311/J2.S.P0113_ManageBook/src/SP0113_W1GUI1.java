    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        String bookCode = this.txtBookCode.getText();
        String bookName = this.txtBookName.getText();
        String author = this.txtAuthor.getText();
        String publisher = this.txtPublisher.getText();
        String publishYear = this.cbbYear.getSelectedItem().toString();
        boolean forRent = this.cbForRent.isSelected();
        if (isUpdate(bookCode)) {
            int pos=findBook(bookCode);
            if(pos!=-1)
            ls.get(pos).setBookName(bookName);
            ls.get(pos).setAuthor(author);
            ls.get(pos).setPublisher(publisher);
            ls.get(pos).setPublishYear(publishYear);
            ls.get(pos).setForRent(forRent);
        } else {
            ls.add(new Book(bookCode, bookName, author, publisher, publishYear, forRent));
        }
        display();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if (ls.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data is empty");
        } else {
            int b = lstBook.getSelectedIndex();
            if (b == -1) {
                JOptionPane.showMessageDialog(this, "Please choose a book");
            } else {
                ls.remove(b);
                display();
            }
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void lstBookMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstBookMouseClicked
        int pos = lstBook.getSelectedIndex();
        txtBookCode.setText(ls.get(pos).getBookCode());
        txtBookName.setText(ls.get(pos).getBookName());
        txtAuthor.setText(ls.get(pos).getAuthor());
        txtPublisher.setText(ls.get(pos).getPublisher());
        cbbYear.setSelectedItem(ls.get(pos).getPublishYear());
        if (ls.get(pos).isForRent()) {
            cbForRent.setSelected(true);
        } else {
            cbForRent.setSelected(false);
        }
    }//GEN-LAST:event_lstBookMouseClicked

