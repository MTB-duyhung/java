    public void display() {
        String[] list = new String[ls.size()];
        for (int i = 0; i < ls.size(); i++) {
            String name = ls.get(i).getBookName();
            list[i] = name;
        }
        lstBook.setListData(list);
    }

    public boolean isUpdate(String code) {
        for (int i = 0; i < ls.size(); i++) {
            if (code.equals(ls.get(i).getBookCode())) {
                return true;
            }
        }
        return false;
    }

    private int findBook(String code){
        for (int i = 0; i < ls.size(); i++) {
            if (code.equals(ls.get(i).getBookCode())) {
                return i;
            }
        }
        return -1;
    }
