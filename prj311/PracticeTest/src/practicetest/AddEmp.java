    public void getList() {
        DefaultTableModel tblModel = (DefaultTableModel) this.tblEmp.getModel();
        tblModel.getDataVector().removeAllElements();
        try {
            conn = ConnectDB.getConnection();
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("Select * from Emp_DE140100");
            while (rs.next()) {
                String empID = rs.getString(1);
                String lastName = rs.getString(2);
                String firstName = rs.getString(3);
                String gender = rs.getString(4);
                String email = rs.getString(5);
                Vector emp = new Vector();
                emp.add(empID);
                emp.add(lastName);
                emp.add(firstName);
                emp.add(gender);
                emp.add(email);
                data.add(emp);
                tblModel.setDataVector(data, header);
            }
        } catch (Exception ex) {
            System.err.println("Get list failure!");
        }
    }


    private boolean validData() {
        String S;
        S = this.txtID.getText().trim().toUpperCase();
        this.txtID.setText(S);
        if (!S.matches("^DE\\d{6}$")) {
            JOptionPane.showMessageDialog(this, "ID: DE + 6 digits.");
            txtID.requestFocus();
            return false;
        }
        S = this.txtEmail.getText().trim().toUpperCase();
        this.txtEmail.setText(S);
        if (!S.matches("^\\w+@\\w+[.]\\w+$")) {
            JOptionPane.showMessageDialog(this, "Email: anyname@any.com");
            txtEmail.requestFocus();
            return false;
        }
        return true;
    }

