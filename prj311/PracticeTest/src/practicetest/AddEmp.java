    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        if (validData()) {
            try {
                conn = ConnectDB.getConnection();
                String sql = "INSERT INTO Emp_DE140100(Emp_id,Last_Name,First_Name,Gender,Email)VALUES(?,?,?,?,?)";
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setString(1, this.txtID.getText());
                stmt.setString(2, this.txtLName.getText());
                stmt.setString(3, this.txtFName.getText());
                String gender = (rbMale.isSelected()) ? "M" : "F";
                stmt.setString(4, gender.toString());
                stmt.setString(5, this.txtEmail.getText());
                int rc = stmt.executeUpdate();
                conn.close();
                getList();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Add failure!");
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

